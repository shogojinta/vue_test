<template>
  <div>
    <h1>bookList</h1>
    <ul>
      <li 
        @click="showBookDetail(book.id)" 
        v-for="book in books" 
        :key="book.id"
      >
        {{ book.title }}
      </li>
    </ul>
  </div>
</template>

<script>
export default {
  name:'BookList',
  data() {
    return {
      bookIndex: -1,
      books: [
        {id: 1, title: 'title1', content: "11111"},
        {id: 2, title: 'title2', content: "22222"},
        {id: 3, title: 'title3', content: "33333"},
      ]
    }
  },
  methods: {
    showBookDetail(id) {
      this.bookIndex = id - 1
      console.log(this.bookIndex);
      this.$router.push({
        name: "Book",
        params: {
          id: this.books[this.bookIndex].id,
          title: this.books[this.bookIndex].title,
          content: this.books[this.bookIndex].content,
        }
      })
    }
  }
}
</script>

